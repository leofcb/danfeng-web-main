// 目的与目标：把 §4.1 全文注入为 system；按轮次追加轻量收口提示。
export function buildSystemText(today, userTurns = 0) {
  let text = BASE.replace('{{TODAY}}', today || new Date().toISOString().slice(0, 10));
  if (userTurns >= 30) {
    text += `\n\n【当前状态】已达 30 轮上限：本轮请礼貌收口——整理本次画像与报告编号，引导客户留微信给持牌顾问，不再展开新话题、不逼单。`;
  } else if (userTurns >= 25) {
    text += `\n\n【当前状态】对话已进行 ${userTurns} 轮（上限 30）：可在作答末尾自然带一句"若要落到具体房源，顾问能更快帮您推进"，但不打断当前问题。`;
  }
  return text;
}

// system blocks（末块标 cache_control:ephemeral，命中 prompt caching）。
export function buildSystemBlocks(today, userTurns = 0) {
  return [{ type: 'text', text: buildSystemText(today, userTurns), cache_control: { type: 'ephemeral' } }];
}
